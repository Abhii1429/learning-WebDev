// Ternary operator or Conditional operator

// syntax=> condition ? expression1 : expression2
// if condition is true, expression1 is executed
// if condition is false, expression2 is executed
 
let age = 8;
let drink = age >= 5 ? "coffee" : "milk";
console.log(drink);
