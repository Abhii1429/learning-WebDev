"use strict"; // Enables strict mode  which helps catch common coding errors

//Intro to Variable:

//variable - container to store data/value
// variables can store some information 
// we can "use" & "change" that information later in our program

//var, let, const - keywords to declare variable
// variable name is Case sensitive
// variable name cannot start with number
// variable name cannot have spaces
// we use camelCase writing style for variable naming

//declare a variable
var firstName = "Abhishek";

//use a variable
console.log(firstName);

// change value
firstName = "Sid";

